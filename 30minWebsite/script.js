const navLinks = document.querySelectorAll("nav a");
const contentElement = document.getElementById("content");

function setActiveLink(element) {
    navLinks.forEach((link) => {
        link.classList.remove("active");
    });
    element.classList.add("active");
}

function setContent(content) {
    contentElement.innerHTML = content;
}

// Initialize content
setContent(purposeContent);

navLinks.forEach((link) => {
    link.addEventListener("click", (event) => {
        event.preventDefault();
        const clickedLink = event.target;

        setActiveLink(clickedLink);

        if (clickedLink.id === "plan") {
            // setContent(planContentUkraine); and after 3s setContent(planContent)
            
            setTimeout(() => {

                setContent(planContent);
            }, 3000);
            setContent(planContentUkraine);
        } else if (clickedLink.id === "purpose") {

            setTimeout(() => {

                setContent(purposeContent);
            }, 3000);
            setContent(purposeContentJapanese);
        } else if (clickedLink.id === "pack") {

            setTimeout (() => {

                setContent(packContent);
            }, 3000);
            setContent(packContentBinary);
        }
    });
});

function updateTimeRemaining() {
    const countdownElement = document.getElementById("countdown-timer");
    const eventDate = new Date("April 8, 2023 09:00:00");
    const currentDate = new Date();
    const timeDifference = eventDate - currentDate;

    if (timeDifference > 0) {
        const days = Math.floor(timeDifference / (1000 * 60 * 60 * 24));
        const hours = Math.floor((timeDifference % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
        const minutes = Math.floor((timeDifference % (1000 * 60 * 60)) / (1000 * 60));

        countdownElement.innerHTML = `${days} days, ${hours} hours, and ${minutes} minutes`;
    } else {
        countdownElement.innerHTML = "Event started!";
    }
}

// Update the countdown timer every minute
updateTimeRemaining();
setInterval(updateTimeRemaining, 60000);


const backgroundClassList = ['background-metsavero', 'background-waves', 'background-woodland-pattern'];
let backgroundIndex = 0;

function cycleBackground() {
  document.body.classList.remove(backgroundClassList[backgroundIndex]);
  backgroundIndex = (backgroundIndex + 1) % backgroundClassList.length;
  document.body.classList.add(backgroundClassList[backgroundIndex]);
}
//onload
cycleBackground();
setInterval(cycleBackground, 10000);

